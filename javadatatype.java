//primitive data types
//1--- boolean type which is True or False
// byte type ----which can have value from -128 to 127(8-bit )
//short type -----are the data in java that range from -32768 to 32767
//int type
//long type
//double type
//float type
//char type
//string type

class datatype{
          public static void main(String args[]){
                    //boolean
                    boolean flag = true;
                    System.out.println(flag +"\n" );

                    //byte type
                    byte range;
                    range = 124;
                    System.out.println(range +"\n");

                    //short type
                    short temp;
                    temp = -200;
                    System.out.println(temp +"\n");
                    
                    // int type
                    int kindo = -4500;
                    System.out.println(kindo +"\n");

                    //long type
                    long num;
                    num = -23344459;
                    System.out.println(num +"\n");

                    //double type
                    double doublenum;
                    doublenum = -34.5;
                    System.out.println(doublenum +"\n");

                    //float type
                    float floatnum;
                    floatnum = -20.4f;
                    System.out.println(floatnum +"\n");


                    //char type

                    char letter;
                    letter = 'a';
                    System.out.println(letter +"\n");

                    //string type

                    String name;
                    name= "rogashian";
                    System.out.println(name + "\n" );

          }
}